package main;

public class StringExercise {
    public static void main(String[] args) {
        String stringFloyd = "We don't need no education\n" +
                "We don't need no thought control";
        System.out.println(stringFloyd);

        String stringCeption = "Check \"this\" out!, \"s inside of \"s!";
        System.out.println(stringCeption);

        String stringDrive = "In windows, the main drive is usually C:\\";
        System.out.println(stringDrive);

        String stringSlashes = "I can do backslashes \\, double backslashes \\\\,\n" +
                "and the amazing triple backslash \\\\\\!";
        System.out.println(stringSlashes);
    }
}
